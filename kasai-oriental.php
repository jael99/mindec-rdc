        <h6 class="color">Economie</h6>

        <p>La province du Kasaï Oriental est une province agro-pastorale disposant des terres arables pour la production vivrière, maraîchère et pérenne, et des plaines et plateaux favorables à l’élevage du gros et petit bétail. Mais l’attrait de l’exploitation artisanale du diamant a provoqué un important mouvement des populations vers des centres d’exploitation du diamant, et par conséquent, l’abandon des activités agricoles rendant ainsi la province dépendante des autres provinces et de l’étranger pour ses besoins alimentaires.</p>
        <p>Jadis grenier du pays, le Kasaï Oriental garde des potentialités de production agricole gigantesques grâce à la fertilité de son sol et à l’abondance des pluies. Ces potentialités ne sont malheureusement pas ou sont très faiblement exploitées aujourd’hui. Elles offrent pourtant des possibilités innombrables.
            La plupart de ses routes, principalement celles de desserte agricole, sont en mauvais état.
            En dehors de l’aéroport national de Bipemba qui constitue la voie autonome rapide et accessible de sortie, la gare de Mwene Ditu, à 120 km environ de Mbuji-Mayi, dans l’actuelle province du Lomami, constitue le point d’attache majeur sur la voie ferrée Sakania-Ilebo qui passe par Lubumbashi, Kamina et Kananga, pour le transport de marchandises.</p>
        <p>L’électricité et l’eau potable constituent un autre point faible de la province. La production hydroélectrique de la centrale de Tshala est insuffisante pour ravitailler la Société minière de Bakwanga (Miba) et la population de toute la province, tout comme la Régideso n’a pas réussi à alimenter convenablement la province et même la ville de Mbujimayi en eau potable.</p>
        <h6>Lacs Mukamba et Fwa</h6>
        <p>Avec ses eaux très claires, le lac Mukamba a la forme physique d’un homme couché avec des membres écartés. La légende affirme que le lac rejette tout objet abandonné sur sa surface. La société Biopharco, filiale de la Miba, utilise l’eau du Lac Mukamba pour la fabrication du sérum. A une distance d’environ 41 km sur le même pourtour, se situe le lac Fwa, très poissonneux. Une belle partie de pêche sportive est possible dans un cadre naturel reposant. Les deux lacs forment la frontière naturelle entre le Kasaï central et le Kasaï oriental.</p>

