        <h2><span class="color">La Journée Internationale des Droits de la Femme 2021

            </br>célébrée au Ministère de la Décentralisation et Réformes Institutionnelles</span>
        </h2>
        <p>
          C’est dans la Salle de réunions du Ministère de la Décentralisation et Réformes Institutionnelles que se sont réunies les femmes des différentes structures du Ministère de la Décentralisation et Réformes Institutionnelles, ce mardi 9 mars 2021 pour échanger autour du thème Le leadership féminin d’excellence : Atout à la réussite de la Réforme de la Décentralisation à l’ère de la COVID-19.»<span id="points">...</span></p>

        <span id="plus">
          <p>Les objectifs visés étaient principalement de comprendre le rôle que peut jouer la femme dans la société pour stopper les effets de la pandémie, mais aussi de relever difficultés que rencontrent les femmes en télétravail, notamment les bouleversements observés dans sa gestion quotidienne du temps qui intègre la prise en compte des charges domestiques, et proposer quelques pistes de solutions quant à ce.
            Au nom de Son Excellence Azarias RUBERWA, Ministre d’Etat, Ministre de la Décentralisation et Réformes Institutionnelles, Maître Françoise KENA, Présidente du Conseil d’Administration de la Caisse Nationale de Péréquation, a ouvert la cérémonie en mettant un accent particulier sur la capacitation de la femme, seul requis pour gravir les échelons pouvant mener à une société effectivement égalitaire.

            Mme Annie KENDA, Directrice chargée des affaires Juridiques au Ministère du Genre, Famille et Enfants, a circonscrit le thème national officiel de l’année, tout en insistant sur la notion du leadership d’excellence.
            Après ces interventions riches de motivation, les participants se sont constitués en groupe de travail à l’issue duquel, ils ont conclu qu’une société égalitaire n’est possible que si toutes les barrières sont levées de toutes parts.
            Les participants ont remercié le Ministre d’Etat, Ministre de la Décentralisation et Réformes Institutionnelles, d’avoir autorisé l’organisation de cette matinée d’échanges et aussi pour la considération toujours démontrée pour la question du Genre.
            La participation des Sieurs Guillaume LELE PERO, et Philippe MAKINDU MASOMONA, tous deux secrétaires généraux, respectivement en charge de la Décentralisation et Réformes institutionnelles a été de riche contributions et très apprécié.
            La cérémonie s’est clôturée par un petit cocktail organisé pour l’occasion.
