          <h4>
            Organisation
          </h4>
          <span class="devider-cont"></span>
          <p>La Constitution de la IIIème République promulguée le 18 février 2006, telle que modifiée par la Loi n°11/002 du 20 janvier 2011 portant révision de certains articles de la Constitution de la République Démocratique du Congo du 18 février 2006, a consacré la décentralisation comme mode de gestion et d’organisation des affaires publiques de l’Etat, à l’inverse de la centralisation excessive de tous les pouvoirs au gouvernement central et de la gestion déléguée qui ont caractérisé la IIème République.</p>

          <p>Cette décentralisation s’est mise en place de manière évolutive quoique butée à quelques résistances liées à la nostalgie de la gestion centralisatrice.</p>

          <p>L’organisation des élections provinciales en 2006, suivie de l’installation des institutions provinciales ainsi que de la tenue du premier Forum National sur la Décentralisation en 2007, ont lancé les premiers signaux forts de la mise en œuvre de la décentralisation.</p>

          <p>Dans son organisation, outre le Cabinet du Ministre d’Etat, trois structures pérennes appuient les actions du Ministère de la Décentralisation et Réformes Institutionnelles. A ces structures, il faudra ajouter la Caisse Nationale de Péréquation, en sigle CNP) qui a ainsi un statut particulier. <br> Instituée par la Constitution en son article 181, la CNP est mise sous la tutelle du Gouvernement conformément à l’article 26 de la Loi organique portant organisation et fonctionnement de la Caisse Nationale de Péréquation. Pour sa mise en œuvre, elle dépend à la fois du ministère de la Décentralisation, du ministère du Plan, du ministère du Budget et du ministère des Finances.</p>

        </article>

