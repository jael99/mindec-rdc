        <h2><span class="color">9ème JOURNÉE AFRICAINE DE LA DÉCENTRALISATION ET DU DÉVELOPPEMENT LOCAL
            </br>Édition 2020</span>
        </h2>
        <p>
          Les Etats membres de l’Union Africaine célèbrent, depuis 2012, la Journée Africaine de la Décentralisation et du Développement Local, le 10 août de chaque année, « en vue, selon l’article 20 de La Charte Africaine sur les Valeurs et Principes de la décentralisation, de la Gouvernance Locale et du Développement Local, de promouvoir les valeurs et les principes de la décentralisation. »<span id="points">...</span></p>

        <span id="plus">
          <p>Pour l’Édition 2020 organisée en Webinaire pour des raisons de la pandémie de Covid-19, cette Journée a été dédiée à la paix sur le continent africain. Elle visait à réaliser une Afrique sans conflits, à prévenir les génocides, à faire de la paix une réalité pour tous et à débarrasser le continent des guerres, des conflits violents, des violations des droits de l’homme et des catastrophes humanitaires. Ainsi, se fixant comme objectif pour l’année 2020 : « Faire taire les armes : créer les conditions propices au développement de l’Afrique », l’Union Africaine a retenu pour thème général :« La contribution des autorités locales à la prévention des conflits et au maintien de la paix sur le Continent Africain», et deux sous-thèmes : «La participation des Collectivités Territoriales d’Afrique au système d’alerte précoce sur les conflits de l’Union africaine » et «La contribution des Collectivités Territoriales d’Afrique à la reconstruction de la paix et de la concorde entre les communautés en situation post-conflit ». </p>

          <p>Le Webinaire a enregistré la participation de quelques 140 personnes représentant plus de 50 Pays d’Afrique et hors Afrique.</p>

          <p>En République Démocratique du Congo, le Ministère de la Décentralisation et Réformes Institutionnelles a célébré cet évènement par l’organisation, au Salon Rouge de l’Immeuble du Gouvernement, d’une Journée scientifique qui a connu la participation de la Vice-Premier Ministre, Ministre du Plan, Mme Elysée Munembwe, du Ministre de la Défense nationale, Emile Ngoy Mukena, du Ministre Délégué chargé des Affaires coutumières, Hérold Mvunzi Meya, du vice-ministre de l’Intérieur, Innocent Bokele Walaka, du vice-ministre des ressources hydrauliques, Papy Pungu ainsi que du secrétaire général à la Décentralisation, Guillaume Léle Pero, du Coordonnateur National de la CTAD, Makolo Jibikilayi ainsi que le gouverneur de la province du Sud Kivu, Théo Ngabije, le gouverneur de la province du Nord-Kivu, Carly Nzanzu Kasivita et le gouverneur de la province d’Ituri, Jean Bamanisa Saidi de nombreux Bourgmestres de la Ville de Kinshasa.</p>
          <p>La cérémonie a connu trois interventions suivies des débats : celles du maitre des céans, le Ministre d’Etat, Ministre de la Décentralisation et Réformes institutionnelles, Me Azarias Ruberwa Manywa, sur le thème général; celle du Ministre Délégué aux Affaires coutumières sur le premier sous-thème, et celle du Coordonnateur national de la Cellule Technique d’Appui à la Décentralisation sur le second sous-thème ainsi que le Mot de clôture de la Vice-Premier Ministre, Ministre du Plan, Représentante personnelle du Premier Ministre.
            Depuis que la célébration de la Première Journée Africaine de la Décentralisation et du Développement local, le 10 août 2012, des activités y relatives ont été organisées sous divers thèmes parmi lesquels : « Financement de la décentralisation, fournitures de services publics et développement local », « La Charte Africaine sur les Valeurs et Principes de la décentralisation, de la Gouvernance Locale et Développement Local », « Egalité des chances, autonomisation socio-économique et participation des jeunes à la gouvernance locale et Développement local en Afrique », « Lutter contre la corruption au niveau local, une voie durable pour transformer l’Afrique à travers ses territoires » et « la contribution des autorités locales à la prévention des conflits et au maintien de la paix sur le continent ».
          </p>
