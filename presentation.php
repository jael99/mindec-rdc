          <h6>I. Organes statutaires de la CNP</h6>
          <p>L’article 3 de la loi organique n° 16/028 du 08 novembre 2016 portant organisation et fonctionnement de la Caisse Nationale de Péréquation prévoit 3 organes ci-après :</p>
          <ul>
            <li> Le Conseil d’Administration ;</li>
            <li>La Direction Générale ;</li>
            <li>Le Collège aux Commissaires aux comptes.</li>
          </ul>
          <h6>II. Les actions dans le cadre de l’opérationnalisation de la Caisse Nationale de Péréquation</h6>
          <p>La mise en œuvre de la CNP a nécessité un certain nombre d’actions, entre autres :</p>
          <ul>
            <li> L’assistance technique en faveur de la CNP ; ;</li>
            <li>La tenue, chaque mardi de la semaine, de réunion de leadership.</li>

          </ul>

          <b>1. Assistance technique</b>
          <p>A l'initiative du Ministre d'Etat, Ministre de la Décentralisation, agissant comme tutelle Admnistrative et Technique,
            la Caisse Nationale de Péréquation a bénéficié de l’appui de la Banque Mondiale à travers le Projet de Développement Urbain,
            PDU en sigle, dans le cadre d'une mission d'assistance technique. Celle-ci a pour mission d'opérationnaliser et de mettre en place
            la Caisse Nationale de Péréquation par le coaching, la production de la documentation et des outils nécessaires à son fonctionnement.</p>
          <p>Par appel d’offre internationale, la firme tunisienne IDEACONSULT INTERNATIONAL a été recrutée pour une assistance technique de cinq mois.
          <p>Depuis décembre 2020, les experts d’IDEACONSULT INTERNATIONAL sont à pied d’œuvre aux côtés des hauts dirigents de la CNP,
            d’experts constitués des points focaux des quatre Ministères clés (Décentralisation et Réformes Institutionnelles,du Plan, Finances, Budget),
            des Ministères sectoriels (Agriculture, du Développement Rural, de la Santé, de l’Enseignement Primaire, Secondaire et Technique,
            de la Formation Professionnelle, Arts et Métiers, de Pêche et Elevage), des Ministères techniques(Urbanisme et Habitat, Infrastructures,
            Travaux Publics et Reconstruction) et des structures (Banque Centrale du Congo ; Projet de Développement Urbain)
            ainsi que la Ville Province de Kinshasa et ses quelques communes(Selembao, N'Djili et Limete) </p>
         
          <b>2. Réunions Hebdomadaires du Mninistre de tutel avec les hauts dirigents de la CNP</b>
          <p>La CNP étant encore au stade d’opérationnalisation, Son Excellence Monsieur le Ministre d’Etat préside chaque mardi,
            la réunion de leadership autour des questions centrales qui concernent la CNP, à savoir la situation du Sous-Compte du Compte Général du Trésor,
            le niveau d’exécution du fonds de péréquation et l’établissement de l’état de déséquilibre de développement.</p>
          <h6>III. Le fonds de péréquation</h6>
          <p>La Caisse Nationale de Péréquation est dotée des ressources financières nécessaires à son fonctionnement et de celles destinées au financement des projets et programmes d’investissement public visant le maintien de la solidarité nationale et la correction du déséquilibre de développement entre les Provinces et entre les ETD.</p>
          <p>C’est cette dernière catégorie des ressources pour les investissements des Provinces et des ETD qui constituent le fonds de péréquation.</p>
          <p>Pour le budget de l’exercice 2020, il a été alloué au titre de fonds de péréquation un montant de CDF 1.082.064.073.706 (mille quatre-vingt-deux milliards soixante-quatre millions septante trois mille sept cents et six mille francs congolais) et avec le contexte de la COVID qui a impacté négativement l’économie du Pays, le Gouvernement de la République s’est résolu avec la loi rectificative des finances publiques de réduire sensiblement à la baisse l’enveloppe dédiée à ce fonds. Malheureusement, suite aux contraintes financières, le Gouvernement n’a pas pu débourser les dits fonds.
          </p>


          <p>Pour l’exercice 2021, l’annexe à la loi des finances ressort un montant de <b>CDF 343.427.994.581</b> au titre de fonds de péréquation.
            Ce fonds est réparti en deux chapitres dont le premier est consacré aux acquisitions d’équipements divers pour un montant de CDF 106.027.298.461 et le second lui est dédié aux constructions diverses pour un montant de <b>CDF 237.400.696.120.</b>
          </p>
