        <h6 class="color">Economie</h6>

        <p>La Province du Kwilu est, autant que sa voisine du Kwango, une entité territoriale à vocation agro-pastorale et son économie repose essentiellement sur l’agriculture, l’élevage et le commerce.</p>
        <p>Les produits agricoles, d’élevage et de pêche du Kwilu sont destinés à plus ou moins 80% au marché de Kinshasa, grand centre de commercialisation et consommation. La Ville de Kikwit demeure l’un des principaux greniers de la ville de Kinshasa, en matière d’approvisionnement en produits vivriers, principalement le maïs, l’arachide et l’huile de palme, et aussi d’élevage et de pêche. La réhabilitation de la Nationale n°1 Kinshasa-Kikwit a amélioré davantage cette position de la nouvelle province du Kwilu par rapport à la Ville de Kinshasa. C’est aussi par Kikwit que transitent les Congolais qui partent des anciennes provinces du Kasaï pour la capitale. </p>
        <p>En dépit de la réhabilitation de la Nationale n°1 qui facilite la traversée de l’ex-Bandundu pour Kinshasa en un temps record et la construction du pont Loango qui relie désormais l’ex-Province du Bandundu à l’ex-Province du Kasaï occidental, la province du Kwilu accuse une dégradation de ses routes de desserte agricole. Ce qui empêche les paysans d’évacuer leurs produits agricoles vers les centres commerciaux. </p>
        <p>Il y a lieu de relever que le développement de cette province passe par la réhabilitation ou la construction des routes de desserte agricole dans tous les territoires du Kwilu.</p>
        <p>Province à vocation agricole, l’attention des dirigeants et opérateurs économiques de cette nouvelle province doit être aussi focalisée sur la relance des activités des industries huilières.
            A ce jour, la province du Kwilu n’a aucune unité de production. Les Huileries et plantations du Kwilu (HPK) à Masi-Manimba, les Plantations Lever au Congo (PLC) à Lusanga ou les Compagnies du Kwilu et de l’Equateur (CKE) à Mokamo et Kimbili qui faisaient la fierté de cet ancien district non seulement pour la production de l’huile ou la création des emplois et l’entretien des routes de desserte agricole ne sont plus opérationnelles depuis plusieurs décennies.
        </p>
