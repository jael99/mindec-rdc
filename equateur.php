        <p>Avec une superficie de 103.902 km2, la Province de l’Equateur est délimitée au Nord par le Sud des Provinces du Sud-Ubangi et de la Mongala ; à l’Est par l’Ouest des Provinces de la Mongala et de la Tshuapa ; au Sud par le Nord de la Province de Mai- Ndombe ; à l’Ouest, par la frontière de la République Démocratique du Congo avec la République du Congo.</p>
