        <h2><span class="color">TENUE DU DEUXIEME FORUM NATIONAL SUR LA DECENTRALISATION</span></h2>


        <p>Il s’est tenu du 11 au 14 décembre 2019 à Kinshasa, Capitale de la République Démocratique du Congo, sous le Haut Patronage de Son Excellence Félix Antoine TSHISEKEDI TSHILOMBO, Président de la République et Chef de l’Etat, le deuxième Forum National sur la Décentralisation. Celui-ci, initié par le ministère de la Décentralisation et Réformes institutionnelles, avait pour thème principal « Evaluation de la décentralisation, treize ans après sa mise en œuvre »<span id="dots">...</span></p>

        <span id="more">
          <p>Plus de 600 participants venus de toutes les Provinces et des pays étrangers ont pris part à cette rencontre nationale. Ce forum a connu quatre temps forts : la cérémonie d’ouverture alimentée par trois allocutions prononcées tour à tour par le Représentant Résident du PNUD, le Ministre d’Etat, Ministre de la Décentralisation et Réformes Institutionnelles et le Président de la République ; les exposés suivis des débats et des travaux en commissions, la lecture du Rapport général et le mot de clôture prononcé par le Premier ministre.</p>
          <p>Premier à intervenir, le Représentant Résident du PNUD, Dominic SAM, a relevé la pertinence d’une synergie d’actions dans la mise en œuvre de la décentralisation en RDC, et proposé trois pistes de réflexion axées sur l’impulsion des actions de changement et de mobilisation des ressources à partir de la province sur base d’un plan de développement comme outil de coordination ; la nécessité de renforcer les capacités des provinces et l’amélioration de la gouvernance locale participative par le renforcement des voies de dialogue avec la population. </p>

          <p>Son Excellence Azarias RUBERWA, Ministre d’Etat en charge de la Décentralisation et Réformes Institutionnelles, a, pour sa part, exprimé sa satisfaction de rassembler, dans une même salle, des délégués venant des entités de base et ceux des institutions nationales jusqu’au Chef de l’Etat, proposant même de consacrer cette date historique du 11 décembre en « journée nationale de la décentralisation ». Jetant un regard sur les 13 ans de mise en œuvre de la décentralisation, il a estimé qu’il y avait lieu de s’interroger sur l’impact qualitatif et quantitatif de cette réforme en termes d’amélioration de la desserte en eau potable, en électricité, de l’offre des services dans les secteurs sociaux de base, de nourriture, etc. </br>
            D’où, a-t-il soutenu la nécessité de poser un diagnostic franc et précis pour s’accorder de meilleures chances de s’engager dans le combat contre la pauvreté à travers la trilogie des trois D : Décentralisation + Démocratie = Développement !
          </p>

          <p>Dans son discours d’ouverture, le Chef de l’Etat Félix Antoine TSHISEKEDI TSHILOMBO, a exprimé ses espoirs de voir la mise en œuvre de la décentralisation aboutir afin d’assurer le plus rapidement possible une amélioration des conditions de vie des Congolais dans les secteurs vitaux que sont la santé, l’éducation et l’emploi. </p>

          <p>A la suite de ces discours, s’est ouverte la série d’exposés.
            Le premier, est celui du Consultant National PNUD, le Professeur Venance OPANGA qui a présenté le thème général du deuxième Forum sur la Décentralisation, ses objectifs globaux et spécifiques et les résultats attendus. Après treize années d’exercice de la décentralisation en RDC sans qu’elle ne fasse l’objet d’une évaluation, il était tout à fait indiqué d’ausculter sa mise en œuvre à travers les sept axes du Cadre Stratégique de Mise en Œuvre de la Décentralisation, CSMOD en sigle.
          </p>

          <p>La deuxième présentation a été faite par le Secrétaire Général de l’Association des Maires du Sénégal et Maire de Ngoudiane, Monsieur MBAYE DIONE. Ce fut un temps de partage de la longue expérience de la décentralisation du Sénégal, vieille de 140 ans. </p>

          <p>Le Secrétaire Général à la Décentralisation, Monsieur Guillaume LELE PERO, est revenu sur l’appropriation effective de la décentralisation, premier axe du CSMOD. Il recommande la réactualisation de la stratégie de communication et l’affectation d’un budget conséquent pour la mise en œuvre de la décentralisation.</p>

          <p>La quatrième intervenante fut la Représentante Pays de l’ONU-FEMMES, Mme Awa NDIAYE SECK a planché sur la prise en compte de la question genre et de l’implication de la jeune fille et de la femme dans tous les cercles décisionnels. </p>

          <p>Le cinquième exposé portant sur le transfert des compétences a été fait par le Coordonnateur National de la Cellule Technique d’appui à la Décentralisation, CTAD en sigle, Monsieur MAKOLO JIBIKILAY. Ce dernier a précisé l’ordre impératif constitutionnel de répartition des compétences entre les deux niveaux du pouvoir, à savoir l’Etat central et les Provinces au travers les articles 201 à 204 de la Constitution.

          </p>

          <p>Il y a lieu d’indiquer que le Coordonnateur National de la CTAD a également exposé sur l’évaluation de l’état des lieux du développement provincial et local au niveau de l’administration, de la gouvernance et du bilan de la décentralisation dans les secteurs décentralisés, à savoir agriculture, développement rural, pèche et élevage, santé, enseignement primaire, secondaire et technique ainsi que formation professionnelle, arts et métiers.</p>

          <p>Le Ministre des Mines, Monsieur Willy KITOBO SAMSONI, a, dans son exposé fixé les règles de répartition de la redevance minière découlant du nouveau Code minier en faveur de l’Etat central, des Provinces et des entités locales du lieu d’exploitation ou d’extraction du gisement minier.</p>

          <p>Le septième exposé dans la thématique a porté sur la Caisse Nationale de Péréquation. Il a été fait par Madame la Présidente du Conseil d’Administration, Maitre Françoise KENA. Des informations utiles sur la Caisse ont été données autour de la mission, des objectifs de ce précieux instrument du Gouvernement et des institutions (Provinces et ETD) bénéficiaires du Fonds de péréquation ainsi que des modalités de sa répartition.</p>

          <p>Madame la Vice-Premier Ministre, Ministre du Plan, Elysée MUNEMBWE a parlé du développement des outils de planification et de gestion. Elle s’est d’abord interrogée sur l’état des lieux de la question avant de brosser les différentes étapes qu’a connues notre Pays en matière de planification, à savoir le Document de la Stratégie sur la Réduction de Pauvreté de la première et deuxième générations mieux connu sous les acronymes DSRP I et II ainsi qu’aujourd’hui, le Programme National de la Stratégie de Développement, PNSD en sigle, qui s’est largement appuyé sur les objectifs du développement durable.</p>

          <p>Le neuvième exposé a porté sur l’harmonisation des processus de décentralisation et de déconcentration. Il a été développé par Monsieur SHABANI BIN KIKUNI, Secrétaire Général à l’Intérieur. « Pas de décentralisation sans déconcentration », a-t-il rappelé avant de recommander vivement aux membres des Exécutifs provinciaux de recourir autant que faire se peut à l’expertise des agents des services déconcentrés à leur disposition.</p>

          <p>Le Gouverneur de la Province du Lualaba, Monsieur Richard MUYEJ MANGEZE MANS, invité à exposer sur la coordination efficace entre l’Etat central et les Provinces et les ETD et les expériences de la Province du Lualaba dans le domaine de développement et gouvernance au niveau provincial et local, a livré aux participants l’expérience de la Province qu’il dirige.</p>

          <p>Le 11ème intervenant fut le Consultant international, Monsieur Félix YE qui a planché sur le renforcement des capacités comme moteur du développement local, partant de l’expérience du Burkina-Faso, son Pays d’origine. Le renforcement doit avoir aujourd’hui un autre langage, celui d’intégrer les aspects organisationnels, institutionnels, communicationnels.</p>

          <p>Le Professeur Adolphe LUMANU BUANA SEFU, ancien Vice-Premier ministre, Ministre de l’Intérieur, Sécurité, Décentralisation et Aménagement du territoire, dans sa réflexion axée sur les rapports de collaboration entre les Institutions provinciales, à savoir l’Assemblée provinciale et l’Exécutif provincial, a plutôt recommandé aux participants de creuser davantage sur la rationalisation des dépenses liées au financement des élections urbaines, municipales et locales et sur l’impact financier résultant du fonctionnement des organes des ETD.</p>

          <p>Le 13ème exposé présenté par le Président de la Ligue Nationale pour les Elections Libres et Transparentes, LINELIT en sigle, Jérôme BONSO, a porté sur la problématique des élections urbaines, municipales et locales. La requalification de ce thème, selon lui, se justifie du fait que depuis les premières élections en 2006, les entités territoriales décentralisées n’ont jamais fait l’objet d’élections pourtant indispensables pour doter ces entités de leurs exécutifs et parlements locaux. Ce qui lui fait dire que le cycle électoral congolais est inachevé.</p>

          <p>L’exposé du Coordonnateur Spécial de l’Aide Internationale et de la Coordination du Groupe des Partenaires, Michel Champredon, a porté sur l’importance de la coopération décentralisée dans le processus de mise en œuvre de la décentralisation. Il a démontré l’importance de la coopération au développement entre les collectivités locales d’un pays avec celles d’un autre pays étranger.</br>

            Le Conseiller juridique du Ministre d’Etat, Ministre de la Décentralisation et Réformes Institutionnelles, Arthur POKA PINZI, a, pour sa part, présenté le rapport synthèse de l’état des lieux du processus de la décentralisation.
          </p>
          <p>
            10 Commissions ont été mises en place, à savoir, l’appropriation effective du processus de décentralisation (1), le transfert des compétences (2), le renforcement des capacités (3), le développement des outils de planification et de gestion (4), l’harmonisation des processus de décentralisation et de déconcentration (5), la coordination efficace entre l’Etat central et les Provinces (6), le financement de la décentralisation et transfert des ressources (7), les rapports de collaboration entre les institutions provinciales (Assemblées provinciales et Exécutifs provinciaux) (8), l’organisation des élections provinciales, urbaines, municipales et locales (9) et l’évaluation des états des lieux de développement provincial et local (10).
          </p>

          <p>Au total, 153 recommandations ont été formulées réparties en celles d’ordre général et celles spécifiques. Parmi les recommandations d’ordre général, l’on peut épingler :</p>
          <ul>
            <li>Le respect et l’application des textes législatifs et réglementaires en matière de décentralisation ;</li>
            <li>Le renforcement des capacités de tous les acteurs-clé de la décentralisation ;</li>
            <li>L’affectation d’un budget conséquent en faveur de la Décentralisation ;</li>
            <li>La concertation permanente entre les trois paliers de gouvernance à savoir, le pouvoir central, la province et l’Entité Territoriale Décentralisée pour une mise en œuvre harmonieuse du processus de la décentralisation ;</li>
            <li>L’irréversibilité du processus de décentralisation en République Démocratique du Congo.</li>
          </ul>

          <p>Au titre des recommandations spécifiques, il y a :</p>
          <ul>
            <li>Le renforcement des campagnes de sensibilisation et de vulgarisation des textes légaux et règlementaires en exploitant les nouvelles approches ;</li>
            <li>L’intégration des notions de la décentralisation dans le programme d’Enseignement Primaire et Secondaire ; </li>
            <li>Le transfert progressif de six (6) nouvelles matières à compétences exclusives des provinces (Environnement, Urbanisme et Habitat, Affaires foncières, Aménagement du Territoire, petit commerce frontalier, coopération interprovinciale) ;</li>
            <li>La mise en place des administrations décentralisées propres des Provinces et des Entités Territoriales Décentralisées ;</li>
            <li> La révisitation de la Loi organique précitée pour intégrer le Ministre ayant la Décentralisation dans ses attributions au bureau de la Conférence en le restructurant ;</li>
            <li>L’installation et l’opérationnalisation des nouvelles Villes et Communes créées et la poursuite de la municipalisation par la création des nouvelles villes et communes d’ici 2021 ;</li>
            <li> Le maintien de la rétrocession avec une période moratoire de 5 ans, puis revenir à la retenue à la source tel que prévu par la Constitution ;</li>
            <li>L’émission par le Ministre des Finances d’un ordre permanent de paiement adressé à la Banque centrale du Congo pour loger dans le sous-compte du Compte Général du Trésor le fonds de péréquation ;</li>
            <li>Le respect de l’engagement formel pour la tenue des élections urbaines, municipales et locales comme prévu dans le projet de loi des finances de l’exercice 2020.</li>

          </ul>
