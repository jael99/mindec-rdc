    <h4 style="text-align: left;">
      Les 26 provinces de la République Démocratique du Congo
    </h4>
    <span class="devider-cont"></span>

    <p>
      Des 4 provinces après le passage de l’Etat Indépendant du Congo au Congo Belge à 6 provinces à la veille de l’indépendance du pays, le nombre de provinces a atteint 21 en 1963 avant de revenir à 9, ensuite 11, pour finalement remonter à 26 provinces, conformément à la Constitution du 18 février 2006. <br>
      Le besoin d’approcher davantage les gouvernants des gouvernés justifie cette réforme territoriale au regard de la superficie du pays qui est de 2.345.409 km2 avec des provinces aussi vastes que bon nombre de pays du monde.
      C’est le cas de l’ancienne province du Katanga avec une superficie de 496.877 km² supérieure à celle de l’Allemagne ou de la Suède ; l’Equateur avec une superficie de 403.462 km2 supérieure à celle de l’Italie ou du Japon ; le Maniema avec une superficie de 132.250 km² supérieure à celle de la Corée du Sud ou du Portugal. Même celle qui était la plus petite province, le Bas-Congo devenu Kongo Central, avec ses 53.920 km², dépasse la superficie de la Belgique ou des Pays-Bas. <br>
      Pourtant ces pays, aux dimensions moindres que la République Démocratique du Congo, avaient plus de provinces que le nôtre. C’est le cas de la France qui, avec ses 550.000 Km², comprend 100 départements ou provinces ; l’Allemagne, avec ses 357.050 Km², a 16 Länders (Provinces) ; l’Espagne qui, avec ses 505.9113 km², comprend 50 provinces ; l’Italie, avec ses 301.336 km², 20 régions et 110 provinces ; le Portugal, avec ses 92.2014 km², 18 régions autonomes, la Suède, avec 449.965 km², 21 comtés ; les Pays-Bas, avec ses 41 530 km², 12 provinces, la Belgique, avec ses 30.528 km², 11 provinces (dont Bruxelles). Or, la RDC, avec ses 2.345.409 km², ne disposait jusque-là que de 11 provinces. Ce qui posa généralement un problème de gouvernance, des grandes parties du territoire n’étant pas réellement administrées. <br>
      La nouvelle carte administrative et territoriale se présente ainsi avec 25 provinces, plus la Ville de Kinshasa qui a aussi le statut de province. Cinq anciennes provinces ont gardé leur statut. Il s’agit de Kinshasa, Kongo central (ex.Bas-Congo) avec Matadi comme chef-lieu, Nord-Kivu avec Goma comme chef-lieu, Sud-Kivu avec Bukavu comme chef-lieu et Maniema avec Kindu comme chef-lieu. Les trois dernières provinces (anciens districts de l’ex-Province du Kivu) ont servi de cobaye d’expérimentation de la nouvelle territoriale depuis 1987. Les 6 autres provinces qui viennent de connaitre le découpage sont :
    <ul>
      <li>Bandundu : Kwango (Kenge), Kwilu (Bandundu), Maï Ndombe (Inongo) ;</li>
      <li>Equateur : Equateur (Mbandaka), Tshuapa (Boende), Mongala (Lisala), Nord-Ubangui (Gbadolité), Sud-Ubangui (Gemena) ;</li>
