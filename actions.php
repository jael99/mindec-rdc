        <h2><span class="color">Tenue du séminaire de renforcement des capacités en faveur des animateurs des institutions politiques provinciales
          </span>
        </h2>
        <p>
          Au lendemain du 2ième cycle électoral pour les législatives provinciales de 2018, le Ministère de la Décentralisation et Réformes institutionnelles a organisé, avec l’appui du PNUD, un séminaire de renforcement des capacités en faveur des animateurs des institutions politiques provinciales (Assemblée provinciale et Gouvernement provincial), anciens et nouveaux, de toutes les provinces du pays.<span id="points1">...</span></p>

        <span id="plus1">
          <p>Ayant pour thème principal « La mise en œuvre de la décentralisation en République Démocratique du Congo, 10 ans après, ses enjeux et perspectives », ce séminaire avait pour objectif général de parvenir à sensibiliser les députés et ministres provinciaux sur leurs rôles et responsabilités et ce, afin d’obtenir l’harmonie dans le fonctionnement de leurs institutions respectives d’une part, et pour ne pas retarder le développement à partir de la base qui est le résultat escompté de la mise en œuvre de la Décentralisation, mais aussi et surtout le crédo du Programme gouvernemental.
            </br>Conçu avec l’appui du PNUD et organisé dans des provinces regroupées en huit pools, il entre dans le cadre du programme multisectoriel et pluridisciplinaire en faveur des députés et ministres provinciaux de l’ensemble de nos Provinces.
          </p>

          <p>Mais, pour des raisons de la pandémie à coronavirus, ce séminaire n’a pas été organisé sur l’ensemble du territoire national. Il ne l’a été que pour le Pool de Kisangani qui a concerné les provinces du Bas-Uele, du Haut-Uele, de l’Ituri et de la Tshopo ; les Pools de Goma et Bukavu qui ont concerné les animateurs des institutions provinciales du Nord et Sud Kivu et dans le Pool de Kananga qui a concerné les députés et ministres provinciaux de la Province du Kasaï Central.</p>

          <p>Ce séminaire a été initié à la suite des difficultés de fonctionnement des institutions politiques provinciales et des crises à répétition qui ont marqué ces deux institutions au cours de la longue législature provinciale passée, 12 ans au total, soit de 2007 à 2019. </p>
          <p>Parmi les recommandations retenues à l’issue de ce séminaire, on cite le besoin d’accélérer le processus du transfert effectif des compétences et des ressources. Pour ce faire, les Ministres du pouvoir central dont les compétences ont été transférées (agriculture, développement rural, enseignement primaire, secondaire et professionnel, santé) doivent se comporter comme de simples coordinateurs de leurs départements ministériels.

            </br>Il a été aussi recommandé la prise urgente des mesures d’application des lois sectorielles ; la réflexion sur les mécanismes d’application de la retenue de 40% et la clarification du rapport entre les provinces et les services déconcentrés en provinces et Entités Territoriales Décentralisées.</p>

