        <p>Le Ministère de la Décentralisation et Réformes Institutionnelles a réalisé au cours de ces dernières années plusieurs actions, avec le financement du Gouvernement ou avec l’appui des Partenaires Techniques et Financiers, notamment la Banque Mondiale, l’Union Européenne, le PNUD, l’ONU Habitat, des représentations diplomatiques avec leurs agences de coopération, etc. Quelques activités importantes réalisées sont signalées à travers les différents articles ci-dessous.
