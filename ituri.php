        <p>La Province Orientale a officiellement éclaté, depuis le 6 juillet dernier, en quatre nouvelles provinces, à savoir : la Tshopo, l’Ituri, le Bas-Uele et le Haut-Uelé. <br>
            L’Ituri est une province dont les originaires ont toujours le plus réclamé la mutation du district (sous-région) en province, référence faite aux travaux de la Conférence Nationale Souveraine et du Dialogue intercongolais. .Elle est, aux côtés de la Tshopo, celle qui a hérité des infrastructures adéquates pour asseoir ses institutions. <br>
            La nouvelle Province de l’Ituri longe la partie Nord-Est de la République Démocratique du Congo sur le versant occidental du Lac Albert. Elle partage les frontières avec, au Nord, la nouvelle Province du Haut-Uélé et la République du Sud-Soudan, à l’Ouest avec la nouvelle Province de la Tshopo, au Sud avec la Province du Nord-Kivu et à l’Est, elle partage une frontière de plus de 600 kilomètres avec la République de l’Ouganda. Avec une superficie de 65 659 km2, la Province de l’Ituri représente 2,79% du territoire national et 13% de la superficie de l’ancienne Province Orientale.</p>

