        <h6 class="color">Economie</h6>
        <p>Tanganyika est réputé pour ses riches gisements d’étain, de cassitérite, coltan, germanium, charbon, calcaire, rhénium, son port sur le lac Tanganyika (35.000 Km2) et ses activités dans la pêche jusque-là semi-industrielle qui occupe une bonne partie de la population. La province abrite également un bassin houillier à Kalemie. <br>
            A cela, il faut ajouter le réseau ferroviaire qui permet de relier Kalemie, Kabalo et Kongolo. Ce qui permet à cette province de ravitailler le reste du pays, principalement les provinces du Lomami, Haut-Lomami, Maniema, Kasaï oriental, Haut- Katanga, etc. en poissons parmi lesquels les célèbres fretins dénommés ndakala. <br>
            Reliée à la province du Lomami et celle du Maniema, la province du Tanganyika est appelée à jouer un rôle de premier plan dans le cadre de la création du marché intérieur. Par conséquent, elle doit développer ses activités agricoles, minières, halieutiques. Cela passe par la réhabilitation et/ou la construction de nouvelles voies de communication (routes, rails, ports). Ainsi, elle pourra servir de voie de sortie aux produits des provinces enclavées du centre du pays (Lomami et Maniema). <br>
            Sur le plan fluvial, on peut exploiter le bief Bukama-Kongolo ouvert à la navigation. Kalemie abrite un aéroport national pour les avions moyens. Sur le plan routier, la réhabilitation de l’axe Mutoto-Kapona-Moba-Mukato-Kalemie (393 km) permettra de relier les lacs Moero et Tanganyika, tous deux foyers de pêche importants aujourd’hui sous-exploités. Elle induira également l’exploitation des potentialités agricoles et l’élevage à Moba. La route Kabondo Dianda-Nyunzu-Kalemie permet, elle, de relier toutes ces grandes agglomérations réputées greniers agricoles et la ville minière de Manono où sont exploités l’or, l’étain et le coltan aux autres provinces de l’ex-Katanga et aux provinces des Kasaï et du Maniema. <br>
            Le développement de cette province passe aussi par la réhabilitation du chemin de fer Kabalo-Kalemie, de la route Manono-Ankoro-Katompi-Ebombo sans oublier l’axe Luambo-Mituaba-Mukwende. La province a également besoin de l’assainissement des réseaux de distribution d’électricité à Kalemie sans oublier l’adduction d’eau dans les grands centres. <br>
            Enfin, le port de Kalemie a besoin d’être dragué pour faciliter la circulation et l’accostage des bateaux et éviter les naufrages enregistrés le long de ce bief navigable. <br>
            Fort de ses cultures industrielles coton, tabac, palmier à huile à Kongolo et Nyunzu, la province peut offrir de milliers d’emplois aux jeunes et attirer de nombreux investisseurs.</p>

