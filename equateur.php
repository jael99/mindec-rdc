        <p>L’ancienne Province de l’Equateur a été créée par décret royal du 1er août 1888 du Roi Léopold II, sous l’appellation initiale de « District de l’Equateur ». Le décret royal susmentionné se référait au décret du 16 avril 1887 portant organisation territoriale de l’Etat Indépendant du Congo.</p>
        <p>Cinq ans avant la promulgation du décret royal en 1888, Henry Morton Stanley, Van Gele et Coquilhat ont installé, au large de la région de Wangata, une station qui devint le chef-lieu de la Province de l’Equateur, chef-lieu qu’on a appelé Equateur-Station, qui deviendra plus tard Coquilhatville, ensuite Mbandaka.</p>
        <p>En 1924, l’Equateur devient une des quatre provinces composant le Congo belge. Cette province sera subdivisée, par l’ordonnance du 5 janvier 1963, en provinces de Cuvette centrale, Moyen Congo et de l’Ubangui avant d’être réunifiées en province unique de l’Equateur comprenant cinq sous-régions ou districts.</p>
        <p>De cette ancienne province de l’Equateur naquirent cinq nouvelles provinces assises sur les cinq anciens districts. Il s’agit de l’Equateur qui reprend l’ancien district de l’Equateur, plus la ville de Mbandaka, de la Mongala qui reprend l’ancien district de la Mongala, du Nord-Ubangui qui reprend l’ancien district du Nord-Ubangui, du Sud-Ubangui qui reprend l’ancien district du Sud-Ubangui, et de la nouvelle province de Tshuapa qui reprend l’ancien district de Tshuapa. L’ancienne province de l’Equateur est celle qui a créé plus de nouvelles provinces que d’autres anciennes provinces démembrées.</p>
