        <h6 class="color">ECONOMIE</h6>
        <p> La Province de la Tshopo est traversée de part en part par le fleuve Congo et Kisangani, ex- Stanleyville, son chef-lieu, est considéré comme le troisième pool économique du pays après Kinshasa et Lubumbashi. <br>
            Posée sur la ligne de l’équateur, la Tshopo connait un climat tropical chaud et humide sans saison sèche. L’agriculture dans la Tshopo est de type de subsistance, elle comprend essentiellement la culture de manioc, de riz, de maïs et de banane plantain alors que la culture des légumineuses (arachides, niébé, soja, haricot,..) reste secondaire et localisée. L’élevage est traditionnel et aucune production animale conséquente n’est présente. <br>
            Les anciennes productions industrielles (palmier à huile, hévéa, canne à sucre, café et cacao) ont été abandonnées et la production actuelle est entièrement assurée par les paysans selon un mode d’exploitation traditionnel. Seule subsiste la fabrication de l’huile de palme à Lokutu par l’entreprise PHC (Plantation Huilerie du Congo) sur les bords du fleuve Congo.</p>
