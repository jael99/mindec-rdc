      <li>Katanga : Haut-Katanga (Lubumbashi), Haut-Lomami (Kamina), Tanganyika (Kalemie), Lualaba (Kolwezi) ;</li>
      <li>Kasaï occidental : Kasaï central (Kananga), Kasaï (Tshikapa) ;</li>
      <li>Kasaï oriental : Kasaï oriental (Mbujimayi), Lomami (Kabinda), Sankuru (Lusambo).</li>
    </ul>

