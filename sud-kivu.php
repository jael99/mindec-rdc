        <h6 class="color">Les infrastructures de transport</h6>
        <p>La province dispose de 1.041 Km de Routes Nationales dont 222 bitumées, 813 km des Routes Provinciales et 2.301 km des routes de desserte agricole. Certaines de ces routes sont en mauvais état. Le Sud-Kivu compte un aéroport, Kavumu, à Bukavu et quelques pistes d’atterrissage.</p>
        <p>La province a comme principales voies navigables les lacs Kivu et Tanganyika qui offrent d’énormes possibilités pour l’évacuation des produits agricoles en toutes saisons. Sur ce trafic, les bateaux ont amélioré ces dernières années leurs services et assurent le transport des biens et des personnes dans d’assez bonnes conditions.</p>
        <h6 class="color">Economie</h6>

        <p>L’économie du Sud-Kivu est essentiellement tournée vers l’agriculture, l’élevage, le commerce et les services.</p>
        <p>L’agriculture est surtout pratiquée par des ménages agricoles. Néanmoins, ces dernières années, on constate une augmentation sensible d’organisations paysannes. Les exploitations de type moderne sont rares. Elles sont tournées essentiellement vers la culture d’exploitation industrielle notamment le café, le thé, la canne à sucre et le quinquina. L’élevage (de gros et petits bétails et de volailles) et la pêche, bien que pratiqués de façon traditionnelle, sont très prospères dans la province.</p>

        <p>En ce qui concerne les mines : l’étain, le wolframite, le monazite, l’or, le colombo tantalite (coltan), le calcaire, le gaz méthane, les eaux thermales et la cassitérite sont exploités actuellement surtout de façon artisanale mais aussi par quelques entreprises modernes (SOMINKI, CIMENKI, …). </p>
        <p>Le Sud Kivu dispose d’un riche potentiel touristique avec le parc national de Kahuzi-Biega et 13 aires protégées qui regorgent de montagnes, d’une flore et d’une faune très rares, notamment les gorilles de montagne, pouvant attirer beaucoup de touristes. L’insécurité due à la guerre et la mauvaise gouvernance font que la plupart de ces sites ne sont pratiquement plus opérationnels.</p>
