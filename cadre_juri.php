        <p>La Constitution du 18 février 2006 consacre la décentralisation comme nouveau mode de gestion des affaires publiques de l’Etat. </p>
        <p>Elle confère à la province et aux Entités Territoriales Décentralisées (ETD) la personnalité juridique qui est traduite par l’autonomie politique et administrative pour les provinces, et l’autonomie administrative pour les ETD. </p>

        <p>Ceci est garanti par l’article 2 de la Constitution qui stipule que la République Démocratique du Congo est composée de la ville de Kinshasa et de 25 provinces dotées de la personnalité juridique.</p>

        <p>L’article 3 de la Constitution dispose que les provinces et les ETD sont dotées de la personnalité juridique et sont gérées par les organes locaux. Ces ETD sont la ville, la commune, le secteur et la chefferie. Elles jouissent de la libre administration et de l’autonomie de gestion de leurs ressources économiques, humaines, financières et techniques.</p>

