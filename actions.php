        <h2><span class="color">10ème Session du Comité National de Pilotage du PDU consacrée à l’examen du rapport annuel 2020 et du projet du Plan de Travail et le Budget Annuel (PTBA) 2021
          </span>
        </h2>
        <p>
          La 10ème session du Comité de pilotage national du Projet de Développement Urbain (PDU) s’est tenue en novembre 2020 dans l’amphithéâtre du Fleuve Congo Hôtel.</p>


        <p>Cette 10ème session du Comité de Pilotage s’est inscrit principalement dans le cadre de l’exécution des activités du projet conformément aux accords de financement signés avec l’IDA, mais dans un contexte particulier, a souligné le Ministre d’Etat, Ministre de la Décentralisation et Réformes institutionnelles. « Le PDU, d’une part, s’est assigné la noble mission d’offrir aux populations urbaines des services sociaux de base et de qualité, tels que les infrastructures routières, scolaires, marchandes et sanitaires. D’autre part, le PDU s’est engagé à pourvoir à la contribution et à l’assimilation des règles et pratiques de bonne gouvernance. Le PDU est actuellement dans une phase très décisive de son cycle car, après sa profonde restructuration, il est tenu à renforcer sa performance en vue de réaliser sa clôture, prévue le 30 Juillet 2021, avec un rapport positif », a-t-il déclaré.<span id="points3">...</span></p>
        <span id="plus3">
          <p>Me Azarias Ruberwa a remercié la Banque Mondiale, à travers le PDU, pour l’appui qu’elle ne cesse d’apporter à la République Démocratique du Congo en général et au Ministère de la Décentralisation et Réformes Institutionnelles, en particulier ; d’avoir recruté, en faveur des Villes, les Consultants en mobilisation des recettes, en gestion des équipements marchands et en évaluation du travail réalisé par l’Equipe Volante de Renforcement des Capacités ; et aussi pour le recrutement, à la demande du ministère de la Décentralisation et Réformes institutionnelles, de la firme tunisienne IDEA-CONSULT INTERNATIONAL chargée d’appuyer la mise en œuvre et l’opérationnalisation de la Caisse Nationale de Péréquation à travers les échanges avec l’équipe dirigeante de la CNP.

          </p>

          <p>En rapport avec les interventions de la Banque mondiale au Ministère de la Décentralisation et Réformes Institutionnelles, le Ministre d’Etat a salué les Equipes de Task-Force Municipales composées des Jeunes Professionnels et des Assistants en Maîtrise d’Ouvrage et Gouvernance Municipale dont ont bénéficié les Mairies de quatre villes phares. « Ce Team permanent dont le Maire en est le Leader, ensemble avec l’équipe de ce dernier, a-t-il dit, permettra à ces quatre villes de relever le défi de la gouvernance notamment l’élaboration de prévisions budgétaires réalistes et la mobilisation des recettes propres en activant le maximum d’actes générateurs des recettes qui relèvent de la compétence de la Ville ».</p>

          <p>Par rapport aux consultants recrutés en faveur des villes, le Ministre d’Etat a noté qu’ils sont déjà à pied d’œuvre parmi lesquels il a signalé la présence d’une firme congolaise, en l’occurrence « Groupement CAAMP ».</p>

          <p>Quant au recrutement de la firme tunisienne IDEA-CONSULT INTERNATIONAL, le Ministre d’Etat en charge de la Décentralisation attend bien voir la Caisse Nationale de Péréquation se doter, à la fin de cette mission, des instruments juridiques et administratifs importants devant contribuer au travail de la CNP, à savoir : résoudre l’équation du déséquilibre de développement de notre pays par la manifestation de la solidarité nationale.</p>

          <p>Revenant sur la 10ème session du Comité de Pilotage National, il a souhaité qu’elle soit le cadre approprié pour entrevoir les voies et moyens devant permettre la matérialisation de ces recommandations dans la mesure du possible, au regard des restructurations intervenues dans le Projet du PDU.</p>
          <p>Ainsi, a-t-il salué l’organisation des multiples cérémonies de réceptions provisoires ou définitives des ouvrages par le PDU dans les Villes Ciblées du Projet, du mois de septembre à novembre 2020. « Je recommande que les réceptions définitives des ouvrages terminés non encore faites, soient absolument programmées avant la clôture du Projet », a-t-il insisté avant de déplorer l’annulation de 70 millions d’USD, intervenue à l’issue de la restructuration du PDU en date du 18 août 2020. Ce qui a provoqué la suppression de certaines activités comme le financement de l’élaboration des Plans Locaux de Développement ainsi que les échanges Sud-Sud pour ne citer que celles-là pour lesquelles le Ministre d’Etat a suggéré au Gouvernement de prendre le relai pour leur réalisation. Il en va du développement du pays à partir de la base, objectif ultime de la grande Réforme qu’est la Décentralisation et crédo inscrit dans le programme commun du Gouvernement que dirige Son Excellence Sylvestre ILUNGA ILUNKAMBA, axé sur quinze piliers dont la vision est « le redressement national à partir de la base ».</p>
          <p>Abordant le chapitre relatif à la composante Gouvernance du Budget annuel 2021, Me Ruberwa a noté avec satisfaction cette affectation des fonds importants pour assurer les bonnes pratiques de gestion, mieux la bonne gouvernance. « C’est à ce prix, a-t-il dit, que nous allons nous inscrire dans la vision du Président de la République, Chef de l’Etat, tel qu’il ressort de son discours d’investiture du 24 Janvier 2019, à savoir « la lutte efficace et déterminée contre la corruption et les antivaleurs, notamment l’impunité, la mauvaise gouvernance, le tribalisme et autres ».</p>

          <p> Il a également exprimé sa satisfaction que quelques projets annoncés par le PDU lors de son dernier séjour à Bukavu au mois de septembre 2020, soient retenus en dépit de la restructuration du Projet pour uniquement le segment relatif aux études techniques. Il s’agit notamment de la réhabilitation du marché de Nyawera, du prolongement de l’avenue Irambo jusqu’au cimetière et de la construction de quelques ponts reliant les axes routiers Kadutu et Bagira. Il en est de même dans les trois autres villes. </p>

