          <p>
          <ul>
            <li>L’état des lieux de la décentralisation est établi dix (10) ans après l’opérationnalisation du Cadre Stratégique de Mise en œuvre de la décentralisation (CSMOD) validé en 2009, décliné en sept (7) axes suivants :
              <ul>
                <li>(Axe 1) appropriation effective du processus de décentralisation ;</li>
                <li>(Axe 2) Transfert des compétences et des ressources suivant une démarche de progressivité ;</li>
                <li>(Axe3) renforcement des capacités ;</li>
                <li>(Axe 4) Développement des outils de planification et de gestion ;</li>
                <li>(Axe 5) harmonisation de la décentralisation et la déconcentration ;</li>
                <li>(Axe 6) Coordination entre l’Etat central et les provinces et</li>
                <li>(Axe 7) financement de la décentralisation.</li>
              </ul>
            </li> <span id="dots">...</span>

          </ul>

          </p>
          <span id="more">
            <ul>
              <li>Des équipes constituées des experts des Ministères impliqués dans la mise en œuvre du processus de la décentralisation [Agriculture, Budget, Décentralisation et Réformes Institutionnelles, Développement Rural, Enseignement Primaire, Secondaire et Professionnel ( EPSP ), Finances, Fonction Publique, Genre, Enfant et Famille, Plan, Santé] ont été ainsi déployées dans les différentes provinces pour effectuer un diagnostic participatif sur le processus de mise en œuvre de la décentralisation dans les entités ciblées.</li>
              <div> <br>
               
              </div><br>
              <li>Quelques acquis sont à l’actif du processus notamment :
                <ul>
                  <li>la validation du CSMOD en juin 2009 ;</li>
                  <li>l’adoption du dispositif légal de la décentralisation ;</li>
                  <li>la mise en place de quelques structures d’impulsion, de pilotage, de coordination et de suivi du processus de décentralisation dont le Conseil National de mise en Œuvre et de Suivi du Processus de la Décentralisation, ainsi que le Comité Interministériel de Pilotage , de Coordination et de Suivi de la mise en œuvre de la Décentralisation, en sigle « CIPCSD » et la Cellule Technique d’Appui à la Décentralisation, en sigle « CTAD, ces deux derniers étant opérationnelles depuis 2008 ;</li>
                  <li>la mise en place des organes provinciaux prévus par la Constitution (Assemblées Provinciales et Gouvernements provinciaux élus) depuis 2007 et qui exercent quelques prérogatives et compétences qui leur ont été dévolues par la Constitution ;</li>
                  <li>le découpage du territoire en 26 provinces dont la ville de Kinshasa, afin de rapprocher les gouvernants des gouvernés, et basé sur 26 provinces ;</li>
                  <li> l’adoption en juillet 2012 d’une stratégie nationale de transfert des compétences et des ressources aux Provinces et aux ETD; </li>
                  <li>l’organisation des premières assises de la coopération décentralisée du 29 au 31 octobre 2012 et l’adoption de la Feuille de route de mise en œuvre de la stratégie nationale de transfert des compétences et des ressources aux Provinces et aux ETD en novembre 2013 ; </li>
                  <li>les opérations de transferts de fonds liés à la part des recettes à caractère national allouées aux provinces en progression continue, la mobilisation par les provinces des fonds propres en augmentation continu pour certaines d’entre elles ;</li>
                  <li>la mise en place de certains services publics provinciaux autonomes dont les régies financières et les régies des infrastructures et travaux publics.</li>
                </ul>
              </li>

              <p>Les états des lieux des provinces ciblées établis par axe démontrent ce qui suit : <br>

              <ul>
                <li>Axe 1 : Une faible appropriation du processus de décentralisation suite entre autres : au manque d’engagement concerté des acteurs dans la mise en œuvre du processus de la décentralisation ; à la connaissance insuffisante des textes légaux et règlementaires ; au manque de volonté politique à tous les niveaux en vue de mettre en œuvre les stratégies et les mécanismes en matière de la décentralisation ; à la faible sensibilisation des acteurs majeurs de la décentralisation, à l’insuffisance des moyens financiers et une stratégie de communication peu opérationnelle la décentralisation, etc. ;</li>
                <li>Axe 2 : Un transfert des compétences et des ressources encore très partiel dû à la non application de la stratégie nationale de transfert des compétences et des ressources aux provinces et aux ETD notamment dans les 4 secteurs prioritaires (Agriculture, Développement Rural, EPSP et Santé) et de sa feuille de route, à l’inexistence des mesures d’application clarifiant les compétences concurrentes entre l’Etat et les provinces conformément à l’art. 203 de la Constitution, application insuffisante des dispositions de la constitution sur la part des recettes à caractère national allouées aux provinces et la caisse nationale de péréquation, la non organisation des élections urbaines, municipales et locales, à l’absence d’édits, des décisions et règlements clarifiant et régissant les attributions des ETD ;</li>
                <li>Axe 3 : Un renforcement des capacités insuffisant et irrégulier à cause de l’absence d'une stratégie nationale de renforcement des capacités des acteurs clés impliqués dans le processus de mise en œuvre de la décentralisation, de l’inexistence des institutions de formation et structures chargées de renforcement de capacités du processus de décentralisation, etc. ;</li>
                <li>Axe 4 : Les outils de planification et de gestion élaborés, mais partiellement disponibles pour certaines provinces et non disponibles pour d’autres, une faible formation d’utilisateurs de ces différents outils, une absence d’approche de gestion accès sur les résultats et le non suivi à la lettre des manuels d'élaboration du budget et des comptes publics… ;</li>
                <li>Axe 5 : Une faible harmonisation du processus de décentralisation et de déconcentration due à l’inexistence de certains textes des lois régissant l’harmonisation des missions et attributions des services du pouvoir central, des provinces et des ETD ; l’inexistence des synergies en vue d’harmoniser des vues entre services décentralisés et les déconcentrés, et l’absence des cadres organiques actualisés pour les services décentralisés et déconcentrés ;</li>
                <li>Axe 6 : Une faible coordination du processus entre l’Etat central et la province due à l’aspect lacunaire de la loi n° 08/015 du 07/10/2008 portant modalités d’organisation et de fonctionnement de la Conférence des Gouverneurs de Province (CGP) notamment la non prise en compte du Ministère ayant en charge la Décentralisation dans ses attributions comme membre du bureau, au non suivi des recommandations de la CGP, à la tenue irrégulière de la CGP, des réunions du Comité Interministériel de Pilotage, de Coordination et de Suivi de la Mise Œuvre de la Décentralisation (CIPCSD) ; à l’absence du Conseil provincial de Décentralisation et à la non implantation de la Cellule Technique d’Appui à la Décentralisation (CTAD) en provinces ;</li>
                <li>Axe 7 : Un financement insuffisant de la décentralisation à cause de la non application des dispositions contenues dans l’ordonnance n°18/004 du 13 mars 2018 fixant la nomenclature des impôts, droits, taxes et redevances de la Province et de l’Entité Territoriale Décentralisée ainsi que les modalités de leur répartition, de la non opérationnalisation de la Caisse Nationale de Péréquation (CNP), du non-respect des dispositions légales relatives aux 40% des recettes à caractère national à allouer aux provinces et aux ETD, de l’incivisme fiscal ; de l’inexistence des édits définissant la clé de répartition des recettes d’intérêt commun entre les provinces et les ETD, de la faible mobilisation des recettes propres des provinces et des ETD.</li>
              </ul>
              </p>
