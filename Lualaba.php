        <h6 class="color">Economie</h6>

        <p>Dans la province du Lualaba, on y trouve des ressources minières telles que le cuivre, le cobalt, le manganèse, le charbon, le germanium, le zinc. L’exploitation de ces minerais nécessite, toutefois, la prise en compte de la protection environnementale, et l’encadrement de la jeunesse souvent tentée par l’enrichissement rapide.</p>
        <p>On y trouve également des cultures vivrières et industrielles. Les principales cultures vivrières sont le maïs, le manioc, le haricot, l’arachide, le riz paddy… Les cultures industrielles, notamment le coton, le tabac, le palmier à huile sont pratiquées dans le territoire de Kapanga.</p>
        <p>La province du Lualaba a l’avantage d’avoir un chemin de fer Lubumbashi-Dilolo et qui se prolonge jusqu’à Lobito en Angola et la présence de la source du fleuve Congo et d’importants gisements miniers. C’est par Dilolo qu’il y a quelques années des millions de tonnes de minerais quittaient la RDC en passant par le port de Lobito pour le marché international. Dilolo, ville-frontière avec l’Angola, sera ainsi un grand centre d’affaires appelé à produire des recettes douanières importantes de par la présence du chemin de fer qui couvrira Fungurume- Tenke-Kolwezi-Kisenge-Divuma-Dilolo jusqu’au port angolais de Lobito.</p>
        <p>Cette position stratégique arrachée par Kasumbalesa et le port de Durban en Afrique du Sud est en voie de revenir à Dilolo. De même que l’embellie enregistrée dans le secteur minier avec l’arrivée de centaines d’investisseurs remet en selle le chemin de fer Dilolo-Lobito. A cet effet, la province dispose d’un réseau routier et ferroviaire qui permet de relier les axes Kasumbalesa-Lubumbashi-Likasi-Kolwezi et Lubumbashi-Likasi-Kolwezi-Dilolo-Kamina.</p>
        

