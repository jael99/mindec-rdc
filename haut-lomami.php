        <p>La population de cette province est constituée de trois grandes ethnies, les Songye, Luba et Kanyok. Il y a également les Kete à Kamiji, les Kanintshin à Luilu et les Lubakat dans le secteur Baluba-Lubangule.</p>

        <h6 class="color">Économie</h6>
        <p>La Province du Lomami est principalement agro-pastorale, avec quelques activités d’exploitation artisanale du diamant à Lubao, Luputa, Kabinda et à Wikong. Outre le diamant qui est intensivement exploité, d’autres substances minérales exploitables existent dont d’importants gîtes des roches carbonatées à Ngandajika, les gîtes d’or dans les environs de Mwene-Ditu et Luputa ainsi que des gites de fer dans le territoire de Kabinda.</p>
        <p>On signale aussi la présence du coltan dans le territoire de Luilu, vers Luputa. Ancien grand producteur du coton, Ngandajika ne garde à ce jour qu’un centre de recherche de coton. <br>
            Pour son développement, la Province du Lomami peut compter sur son agriculture et son élevage dont une bonne production provient de tous les cinq territoires et sa riche forêt en bois au nord de Kabinda.
            Le plus grand handicap est le moyen de communication, pourtant, partageant actuellement les limites avec sept autres provinces, les échanges commerciaux passent par l’amélioration prioritaire des moyens de communication (route, rail, piste d’atterrissage, rivière).</p>
        <p>A ce jour, la route Kabinda-Ngandajika-Luputa-Mwene Ditu doit être réhabilitée et asphaltée en priorité, tout comme celle de Kabinda-Mbujimayi-Mweneditu, de Kabinda-Lubao pour favoriser les échanges, essentiels pour redynamiser la production agricole et les échanges. C’est aussi le cas du rail Lubumbashi-Mwene Ditu utile pour promouvoir les échanges avec le grand Katanga, et pourquoi pas, le reste de l’Afrique australe.</p>
        <p>Le seul aéroport de Kabinda qui n’a même pas de service de Régie de Voies aériennes (RVA) ne suffit pas pour toute la province. Il y a lieu de pourvoir la province en pistes d’atterrissage fiables à Mwene-ditu, Ngandajika, Lubao… pour bien désenclaver la province. Les conditions de navigation sur la rivière Lomami par le bac à partir de Lubao méritent aussi d’être améliorées afin de relier la province du Lomami avec celle du Haut-Lomami. <br>
            L’électricité publique et l’eau potable n’existent pratiquement pas sur l’ensemble de la Province. Les services SNEL et REGIDESO doivent être non seulement améliorés, mais surtout installés dans beaucoup de localités de la province. Des sites susceptibles d’être exploités pour l’électrification urbaine et rurale par les micros centrales sont disponibles.</p>
