        <p>La Constitution de la République a, en son article 181, institué la Caisse Nationale de Péréquation, CNP en sigle. En exécution des dispositions de cet article, la loi organique n° 16/028 du 08 Novembre 2016 a ainsi fixé l’organisation et le fonctionnement de cette Caisse. Le 28 décembre 2018, le Président de la République a, par Ordonnance n°18/149 du 27 décembre 2018 portant nomination des membres du Conseil d’Administration et de la Direction Générale de la CNP, nommé les Membres du Conseil d’Administration et de la Direction Générale<span id="points">...</span></p>
        <span id="plus">
