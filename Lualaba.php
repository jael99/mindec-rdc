        <p> Située à l’Est du Katanga, la Province du Lualaba a une superficie de 121.309 km2 et partage des frontières avec la République d’Angola et la République de Zambie (au Sud) et les provinces du Kasaï central, du Lomami (au Nord) et du Haut-Katanga (à l’Est).</p>
        <p>Elle abrite la source du deuxième fleuve le plus puissant au monde par son débit (40.000 m3/minute), véritable colonne vertébrale du Grand Congo qu’il traverse du sud au nord pour se déverser dans sa partie ouest sur l’océan Atlantique.</p>


