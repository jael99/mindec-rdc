        <p>La Province du Haut-Katanga est riche en ressources minières, agricoles, douanières et touristiques. Elle est caractérisée par une forte concentration de produits miniers (cuivre, cobalt, germanium, plomb, cadmium, étain, coltan, fer, charbon, cassitérite…). Les gisements connus attirent de nombreux exploitants mais les recherches géologiques doivent être poursuivies pour déterminer la valeur de nouvelles exploitations.</p>
