          <h4 style="text-align: left;">
            Les 23 Ministres en Charge de la Décentralisation
          </h4>
          <span class="devider-cont"></span>
          <p>La décentralisation n’est pas un phénomène nouveau en République Démocratique du Congo. En dehors de certaines tentatives de la décentralisation datant de l’époque coloniale, la réforme de la territoriale aboutit, en 1982, à la promulgation d'une loi organique portant décentralisation administrative. La région (province) et la zone (territoire ou commune) sont devenues des entités administratives décentralisées, avec des assemblées régionales et des conseils de zone comme organes délibérants. Mais, en 1997, à la chute du Président Mobutu, cette loi était pratiquement restée lettre morte.
          </p>
          <p>En 1998, une réforme fut mise en place, au terme du décret-loi 081, qui définit une stratégie visant la décentralisation. Cependant, en 2006, la décentralisation n'était toujours pas appliquée selon les prescrits de ce Décret-loi.
          </p>
          <p>La Constitution du 18 février 2006 consacre la décentralisation comme nouveau mode de gestion administrative et territoriale de l’Etat. Elle institue deux niveaux du pouvoir d’Etat : le pouvoir central et les provinces et trois paliers de gouvernance, à savoir le pouvoir central, la province et les entités territoriales décentralisées. Elle confère le statut d’entité territoriale décentralisée à la ville, la commune, la chefferie et au secteur. Dans ce long processus de décentralisation plein d’embûches et de balbutiements, plusieurs ministres ont eu la charge de mettre en œuvre la gestion décentralisée du pouvoir d’Etat en qualité tantôt de commissaire d’Etat (ministre) de l’administration du territoire, tantôt de ministre de l’intérieur et sécurité ou de ministre de l’intérieur et décentralisation, voire de ministre de la décentralisation. Ainsi, les personnalités ci-après ont eu à occuper le portefeuille en charge de la décentralisation :
          </p>



