        <h2><span class="color">Tenue de la 9ème Session du Comité National de Pilotage du Projet de Développement Urbain, « PDU »
          </span>
        </h2>
        <p>
          En date du 15 juillet 2020, il a été organisé à ROMEO GOLF de Kinshasa les travaux de la 9ème Session du Comité National de Pilotage du Projet de Développement Urbain, « PDU » en sigle, portant sur « l’état d’avancement des activités du Projet par rapport au Programme de Travail et Budget Annuel (PTBA) 2020 adopté à la précédente session du 30 décembre 2019 ».<span id="points2">...</span></p>

        <span id="plus2">
          <p>Ont pris part à ces travaux, en leurs qualités respectives de président et vice-président du comité national de pilotage du PDU, le Ministre d’Etat, Ministre de l’Urbanisme et Habitat, Monsieur Pius Mwabilu et le Ministre d’Etat, Ministre de la Décentralisation et Réformes Institutionnelles, Me Azarias Ruberwa ; les Secrétaires généraux des ministères membres du Comité, la Présidente du Conseil d’Administration de la Caisse Nationale de Péréquation, le Directeur Général de la Caisse Nationale de Péréquation et son Adjoint, les Directeurs de Cabinet, Directeurs de Cabinet Adjoint et Conseillers des différents Ministères sectoriels, les Experts des ministères et services sectoriels ainsi que le Représentant de la Société Civile.</p>

          <p>Dans son discours d’ouverture, Son Excellence Monsieur le Ministre d’Etat, Ministre de l’Urbanisme et Habitat a relevé le contexte de la pandémie de Covid-19 et de l’épidémie à virus Ebola qui a conduit à la restructuration du Projet par l’affectation de 50 millions USD à la lutte contre cette épidémie, de la réduction du champ des opérations du Projet à quatre villes phares (Matadi, Kikwit, Goma et Bukavu), de la requête complémentaire visant à obtenir 5 millions USD pour la lutte antiérosive au bénéfice de la ville de Kananga.</p>

          <p>Il a enfin signalé la grande restructuration du Projet par l’annulation avant l’échéance de certains projets à problème à concurrence de 70 millions USD en vue d’assainir le portefeuille des projets et créer ainsi un statut favorable à l’octroi d’autres financements pour l’année 2021.</p>
          <p>Son Excellence Monsieur le Ministre d’Etat, Ministre de la Décentralisation et Réformes Institutionnelles a pris la parole à son tour, pour encourager les membres de la Coordination pour l’exercice de leur mission depuis que le PDU a démarré en 2013. Il a également relevé l’importance de ce Projet dans le développement local, car le PDU cible les villes, une des Entités Territoriales Décentralisées.</p>

          <p>Il a poursuivi en disant que le développement urbain est une préoccupation de tous les Etats de la planète, car selon un rapport de la Banque Mondiale, 55% de la population mondiale, soit 4,2 milliards de personnes vivent en ville avec tout ce que cela implique comme nombreux défis à relever. Il a salué l’intervention de la Banque Mondiale à travers ce Projet qui cadre avec la vision du Chef de l’Etat et celle du Gouvernement d’un développement national à partir de la base.</p>

          <p>Me Azarias Ruberwa a cependant relevé que le renforcement des capacités institutionnelles et humaines reste encore faible dans la composante en rapport avec la gouvernance urbaine et a rappelé quelques recommandations formulées lors du 2ème Forum National sur la Décentralisation du 11 au 14 décembre 2019 à Kinshasa. Il a aussi exhorté tous les acteurs impliqués dans le Projet, à tout faire dans l’intervalle de temps restant, pour atteindre les objectifs assignés en dépit du contexte dominé par la pandémie Covid-19.</p>
          <p>Il a terminé son intervention en évoquant la Caisse Nationale de Péréquation qui est en fait la cheville ouvrière d’un développement à la base, tout en remerciant le PDU pour son assistance dans le recrutement d’un cabinet d’assistance technique en faveur de la Caisse. Mais aussi, en lançant un appel à la Banque Mondiale pour une contribution au fonds de péréquation.</p>
          <p>Après ces allocutions d’orientation, la présentation du rapport des activités du PTBA au 31 juin 2020 a attiré l’attention de l’auditoire. On a retenu, dans le secteur de la Santé, les effets de la riposte à la Covid-19 avec comme conséquence la modification de certaines dispositions du Manuel des Procédures parce que cette activité n’était pas prévue au démarrage du PTBA. Dans le domaine de la voirie, la préparation à l’intervention du PDU dans les travaux de lutte antiérosive dans la ville de Kananga ; et dans le domaine financier, la préparation à l’annulation des Fonds sollicités par le Ministre des Finances à la Banque Mondiale le 02 juillet 2020, par l’adaptation du PTBA ; en prévision de la clôture anticipative en Décembre 2021 au lieu de Novembre 2022.</p>
          <p> Les activités du PTBA 2020 révisé présentées par le Coordonnateur du PDU, Monsieur DAKAHUDINO, suivi des experts des différents services se résument, dans le domaine de la voirie, par la réception provisoire prévue et la poursuite des travaux ; dans le domaine de la passation des marchés, par la sélection des projets pour les études environnementales et sociales ; dans le domaine de la Gouvernance Urbaine, par la construction des infrastructures de proximité, le recrutement des consultants Assistants Municipaux et le renforcement des capacités en matière de mobilisation des recettes locales ; et concernant la Caisse Nationale de Péréquation, par l’évolution des travaux de la Commission d’évaluation de l’Offre technique et financière qui est en cours d’évaluation.</p>
          <p>Le PDU a tenu à préciser que les hypothèses initiales du PTBA 2020 ont été entamées par plusieurs facteurs non prévus, notamment la riposte contre la maladie à virus Ebola et l’intervention dans la lutte antiérosive dans la ville de Kananga.</p>
          <p>A la suite de ces travaux, des recommandations ci-après ont été formulées et présentées par le Coordonnateur du PDU.</p>
          <ul>
            <li>La Ville de Goma, qui n’a été ajoutée au projet qu’à partir de 2017 avec le financement additionnel, n’a pas encore bénéficié d’une infrastructure (voirie ou bâtiments), le CPN recommande qu’un montant de 5 millions soit alloué à cette ville dans la rubrique « dépenses à engager » dans la restructuration en cours de finalisation.</li>
            <li>Pour l’ensemble du Projet, comme d’autres projets financés par la Banque Mondiale vont jusqu’au 4ème financement additionnel, le CPN recommande qu’une requête conjointe de Leurs Excellences de la Décentralisation et de l’Urbanisme et Habitat soit transmise au Ministre des Finances pour un 2ème financement additionnel d’au moins 80 millions d’USD avec respect de la date de clôture du Projet à décembre 2021 ;</li>
            <li>Pour le travail réalisé ayant abouti au choix du PDU pour l’annulation des 70 millions d’USD, le Comité de Pilotage du PDU recommande que la Cellule de Suivi des Projets et Programmes (CSPP) puisse organiser des séances d’évaluation des projets pour examiner ensemble les solutions aux problèmes rencontrés par le Projet.</li>
          </ul>
          <p>Le Comité de Pilotage a recommandé également que l’interface du projet à la Banque Mondiale veille à ne pas freiner l’élan de l’exécution du Projet. Au vue de grandes décisions prises, notamment l’annulation des 70 millions d’USD, il aurait fallu soumettre la proposition au préalable au CPN qui, dans les accords de financement, a pour mission de formuler des directives générales pour le Projet. Lorsque les fonds annulés et gardés dans l’enveloppe IDA nationale de la RDC deviendront opérationnels, le CPN recommande que la Caisse Nationale de Péréquation soit bénéficiaire des fonds qui seront réattribués dans l’exercice fiscal 2021.</p>
          <p>Dans son mot de clôture, le Ministre d’Etat, Ministre de l’Urbanisme et Habitat, Monsieur Pius Mwabilu a rassuré l’assistance que lui-même et son Collègue de la Décentralisation et Réformes Institutionnelles, ont résolu fermement de s’impliquer pour apporter efficacement leur contribution face aux faiblesses relevées par le Coordonnateur du PDU. </p>

