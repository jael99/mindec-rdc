        <span class="devider-cont"></span>

        <p> Avant 1960, la province du Kasaï Oriental faisait partie de la Province du Kasaï qui avait Luluabourg, actuelle Kananga, comme chef-lieu. A la suite des conflits qui opposent à partir de 1959, les Luluas aux Lubas, ces derniers quittent Luluabourg pour s’installer autour des concessions de la Minière de Bakwanga (MIBA) et dans les villages environnants. En août 1960, Albert Kalonji proclame l’Etat autonome du Sud-Kasaï avec Bakwanga qui deviendra plus tard, Mbuji-Mayi, comme chef-lieu. A son origine, cette nouvelle province a été essentiellement constituée suite à l’exode massif des Baluba qui venaient de plusieurs provinces dont le grand Kasaï et le Katanga où la sécession avait éclaté dès le 11 juillet 1960. C’est finalement par ordonnance n°66/205 du 6 avril 1966 que l’ancienne Province du Kasaï oriental fut créée. Cette province vient de céder la place à trois nouvelles provinces créées par la Loi organique n°015/006 du 15 mai 2015 fixant les limites des provinces et celles de la Ville de Kinshasa. Il s’agit de la Lomami, du Sankuru et de la nouvelle province du Kasaï oriental.</p>

        <p>Avec ses 9.545 km2, la Province du Kasaï oriental actuel est la désormais plus petite province de la République en terme de superficie, moins que la Ville-Province de Kinshasa qui a une superficie de 9.965 km2, et dont l’enclavement multiplie et renforce les défis à relever. Elle comprend l’ancien district de Tshilenge, plus la Ville de Mbujimayi. <br>
            Entièrement enclavée, elle est située au centre du pays, délimitée au Nord par l’Est de la Province du Kasaï Central et le Sud de celle du Sankuru ; à l’Est par l’Ouest de la Province de Lomami et le Sud de la Province du Sankuru ; au Sud par l’Ouest de la Province de Lomami ; à l’Ouest par l’Ouest de la Province de Lomami et l’Est de la Province du Kasaï Central.</p>

